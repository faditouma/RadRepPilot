import { prototypeSafetyNote, sourceLinks } from './sourceMetadata';

const yesNo = [
  { value: '', label: 'Not specified' },
  { value: 'no', label: 'No' },
  { value: 'yes', label: 'Yes' },
];

const commonNegatives: Record<string, OneClickNegative> = {
  noTrauma: { label: 'No trauma', values: { trauma: 'no' } },
  noAnticoag: { label: 'Not anticoagulated', values: { anticoagulation: 'no', antiplateletAnticoagulation: 'no' } },
  noNeuro: { label: 'No focal neuro deficit', values: { neuroDeficit: 'no', abnormalNeuroExam: 'no' } },
  afebrile: { label: 'Afebrile', values: { fever: 'no' } },
  noPregnancy: { label: 'No pregnancy concern', values: { pregnancyStatus: 'No pregnancy concern' } },
  noSurgery: { label: 'No prior surgery', values: { priorSurgery: 'No prior surgery' } },
  noContrastAllergy: { label: 'No contrast allergy', values: { contrastSafety: 'No contrast allergy reported' } },
  egfrOk: { label: 'eGFR acceptable', values: { contrastSafety: 'eGFR acceptable for contrast if required' } },
  noCancer: { label: 'No cancer history', values: { cancerHistory: 'no', cancerImmunosuppression: 'no' } },
  noImmunosuppression: { label: 'No immunosuppression', values: { immunosuppression: 'no', cancerImmunosuppression: 'no' } },
};

function text(id: string, label: string, placeholder?: string): PrimaryCareFieldDefinition {
  return { id, label, type: 'text', placeholder };
}

function area(id: string, label: string, placeholder?: string): PrimaryCareFieldDefinition {
  return { id, label, type: 'textarea', placeholder };
}

function yn(id: string, label: string): PrimaryCareFieldDefinition {
  return { id, label, type: 'select', options: yesNo };
}

function template(args: {
  id: string;
  title: string;
  bodySystem: BodySystem;
  modality: string;
  common?: boolean;
  description: string;
  quickFields: PrimaryCareFieldDefinition[];
  essentials: string[];
  negatives?: OneClickNegative[];
  why: string[];
  questions: string[];
}): PrimaryCareContentTemplate {
  const baseDetailed = [
    {
      id: 'symptoms',
      title: 'Symptoms',
      fields: [text('age', 'Patient age', 'e.g. 45-year-old'), text('duration', 'Duration'), area('positiveSymptoms', 'Pertinent positives'), area('negativeSymptoms', 'Pertinent negatives')],
    },
    { id: 'redFlags', title: 'Red flags', fields: [area('redFlags', 'Red flags / high-risk features')] },
    { id: 'exam', title: 'Exam', fields: [area('examFindings', 'Relevant exam findings')] },
    { id: 'labs', title: 'Labs', fields: [area('labs', 'Relevant labs')] },
    { id: 'history', title: 'Relevant history', fields: [area('pmhx', 'Relevant PMHx / risk factors'), area('priorImaging', 'Prior imaging / relevant history')] },
    { id: 'safety', title: 'Contrast/safety', fields: [text('pregnancyStatus', 'Pregnancy status when relevant'), area('contrastSafety', 'Renal function / contrast allergy / metformin when relevant')] },
    { id: 'question', title: 'Question for radiology', fields: [area('clinicalQuestion', 'Exact clinical question')] },
  ];

  return {
    id: args.id,
    title: args.title,
    bodySystem: args.bodySystem,
    modality: args.modality,
    commonInPrimaryCare: args.common ?? true,
    description: args.description,
    quickFields: args.quickFields,
    detailedFieldSections: baseDetailed,
    essentialFields: args.essentials,
    oneClickNegatives: args.negatives ?? [],
    whyItMatters: args.why,
    suggestedClinicalQuestions: args.questions,
    defaultQuestion: args.questions[0],
    sourceNames: ['ACR Appropriateness Criteria'],
    sourceLinks: [sourceLinks.acrAppropriateness],
    safetyNote: prototypeSafetyNote,
  };
}

